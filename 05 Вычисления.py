# Task 14
"""
«Автопробег»
За день машина проезжает n(day) километров. Сколько дней нужно, чтобы проехать маршрут длиной m(total) километров?
Input:  700
        750
Output: 2
"""
day, total = (int(input())for _ in range(2))

print(total // day + bool(total % day))

# Оригинальное решение
print((total - 1) // day + 1)


# Task 15
"""
«Улитка»
Улитка ползёт по вертикальному шесту высотой h метров, 
поднимаясь за день на a метров, а за ночь спускаясь на b метров. 
На какой день улитка доползёт до вершины шеста? Программа получает на вход натуральные числа 
Гарантируется, что a > b.
Input:  10
        3
        2
Output: 8
"""
h, up, down = (int(input()) for _ in range(3))
day = 1

while up < h:
    h -= up - down
    day += 1

print(day)


