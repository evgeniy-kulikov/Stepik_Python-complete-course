# Task 03
"""
«Сумма цифр»
Дано натуральное трехзначное число. Найдите сумму его цифр.
"""
n = int(input())
res = 0

while n:
    res += n % 10
    n //= 10

print(res)


# Task 09
"""
«Конец уроков»
Начало в 9:00. Продолжительность урока — 45 минут.
после 1-го, 3-го, 5-го и т.д. уроков перемена 5 минут
после 2-го, 4-го, 6-го и т.д. — 15 минут
Дан номер урока (число от 1 до 10). Определите, когда заканчивается указанный урок. 
Выведите два целых числа: время окончания урока в часах и минутах.
"""
n = int(input())

start = 9
school_breaks = 20 * ((n - 1) // 2) + 5 * ((n - 1) % 2)
total = 45 * n + school_breaks

h = start + total // 60
m = total % 60

print(h, m)


# Task 10
"""
«Бактерия»
Бактерия, попав в живой организм, к концу каждой минуты делится на две бактерии, 
каждая из них к концу следующей минуты делится опять на две и так далее. 
Сколько бактерий окажется в организме через t минут, если в организм попадут n бактерий. 
Input:  10
        1
Output: 1024
"""
t, n = int(input()), int(input())

while t:
    n *= 2
    t -= 1

print(n)

# Вариант
print((2 ** t) * n)


# Task 11
"""
«Кинотеатр»  (Сумма арифметической прогрессии)
в каждом следующем ряду (начиная с первого) количество мест на одно и то же значение d больше, чем в предыдущем. 
В первом ряду  a  мест, всего  n  рядов. Определите, количество мест в кинотеатре. 
Значения d, a, n вводятся подряд, каждое с новой строки. d - некоторое натуральное число.
Input:  1
        1
        3
Output: 6
"""
d, a, n = (int(input()) for _ in range(3))
total = 0

while n:
    total += a
    a += d
    n -= 1
print(total)

# Вариант
for el in range(n):
    total += a + el * d
print(total)

# Короче
print(sum(el * d + a for el in range(n)))


